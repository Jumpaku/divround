package main

import (
	"fmt"
	"math"
)

func main() {
	for _, a := range []int64{-25000000000000001, -25000000000000000, -24999999999999999, 25000000000000001, 25000000000000000, 24999999999999999,
		-35000000000000001, -35000000000000000, -34999999999999999, 35000000000000001, 35000000000000000, 34999999999999999} {
		for _, b := range []int64{-10000000000000000, 10000000000000000} {
			if b == 0 {
				continue
			}
			f := float64(a) / float64(b)
			fmt.Printf("{\n%d,\t%d,\n%d,\n}, // %f\n", a, b, int(math.RoundToEven(f+1)-1), f)
		}
	}
}
